  // –°–ø–∏—Å–æ–∫ —Ä–∞–∑—Ä–µ—à–µ–Ω–Ω—ã—Ö HTTP –º–µ—Ç–æ–¥–æ–≤ –¥–ª—è CORS –∑–∞–ø—Ä–æ—Å–æ–≤
