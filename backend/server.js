// –í–∫–ª—é—á–∞–µ–º –ø–∞—Ä—Å–∏–Ω–≥ JSON –¥–∞–Ω–Ω—ã—Ö –≤ —Ç–µ–ª–µ –∑–∞–ø—Ä–æ—Å–∞ —Å –ª–∏–º–∏—Ç–æ–º 10MB
