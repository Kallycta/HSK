// –ü—Ä–∏–º–µ–Ω—è–µ–º –æ–≥—Ä–∞–Ω–∏—á–∏—Ç–µ–ª—å —Ç–æ–ª—å–∫–æ –∫ API –º–∞—Ä—à—Ä—É—Ç–∞–º (–Ω–µ –∫–æ –≤—Å–µ–º—É —Å–∞–π—Ç—É)
